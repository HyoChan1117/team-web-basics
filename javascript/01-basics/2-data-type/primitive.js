// 1. number
let a = 10;
let b = 3.14;
let c = Infinity;
let d = NaN;

console.log(a)  // 10
console.log(b)  // 3.14
console.log(c)  // Infinity
console.log(d)  // NaN


// 2. string
let name = '아기고양이';
let msg = "Hello World";
let greet = `Hi, ${name}`;

console.log(name);  // 아기고양이
console.log(msg)    // Hello World
console.log(greet)  // Hi, 아기고양이


// 3. boolean
let isOnline = true;
let hasError = false;

console.log(isOnline);  // true
console.log(hasError);  // false


// 4. null
let x = null;

console.log(x);


// 5. undefined: 값이 아직 정의되지 않음
let y;
console.log(y);