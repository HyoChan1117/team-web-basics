// 비교 연산자
// 두 값을 비교하고 결과는 항상 true 또는 false
// ==, ===, !=, !==, >, <, >=, <=
// ===와 !==는 자료형까지 비교
const x = "5";

console.log(x == 5);   // true
console.log(x === 5);  // false
console.log(x != 5);   // false
console.log(x !== 5);  // true
console.log(x > 2);    // true
console.log(x < 5);    // false
console.log(x >= 5);   // true
console.log(x <= 2);   // false