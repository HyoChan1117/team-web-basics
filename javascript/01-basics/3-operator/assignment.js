// 대입 연산자
// =, +=, -=, *=, /=, %=
let x = 5;
console.log(x);  // x = 5

x += 5;
console.log(x);  // x = x + 5 -> x = 5 + 5

x -= 5;
console.log(x);  // x = x - 5 -> x = 10 - 5

x *= 2;
console.log(x);  // x = x * 2 -> x = 5 * 2

x /= 2;
console.log(x);  // x = x / 2 -> x = 10 / 2

x %= 2;
console.log(x);  // x = x % 2 -> x = 5 % 2